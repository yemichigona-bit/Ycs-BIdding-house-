// Generate bids for listings
export const generateBids = (): Bid[] => {
  const bids: Bid[] = [];
  const buyers = ['Jamie', 'Taylor', 'Morgan', 'Casey', 'Riley', 'Jordan', 'Yemson', 'Sam'];
  
  listings.forEach((listing) => {
    const numBids = Math.floor(Math.random() * 5) + 2;
    let currentAmount = listing.startingPrice;
    
    for (let i = 0; i < numBids; i++) {
      currentAmount += Math.floor(Math.random() * 20) + 5;
      bids.push({
        id: `bid-${listing.id}-${i}`,
        listingId: listing.id,
        userId: i === numBids - 1 ? 'buyer-1' : `user-${Math.floor(Math.random() * 100)}`,
        userName: i === numBids - 1 ? 'Sam (Buyer)' : buyers[Math.floor(Math.random() * buyers.length)],
        amount: currentAmount,
        timestamp: new Date(Date.now() - 1000 * 60 * Math.floor(Math.random() * 120)),
      });
    }
    
    // Update listing with bids and current price
    listing.bids = bids.filter(b => b.listingId === listing.id).sort((a, b) => b.amount - a.amount);
    if (listing.bids.length > 0) {
      listing.currentPrice = listing.bids[0].amount;
      listing.bidCount = listing.bids.length;
    }
  });
  
  return bids;
};

